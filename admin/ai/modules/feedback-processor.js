'use strict';

/**
 * @fileoverview AI-powered user feedback processor for the Milonexa admin agent.
 *
 * Capabilities:
 *  - Reads feedback entries from .admin-cli/feedback/ (JSON files, one per entry).
 *  - Accepts raw feedback pushed via the admin REST-API (POST /feedback).
 *  - Uses the local Ollama LLM to:
 *      • Categorize each item (bug, feature_request, ux_issue, performance, praise, other).
 *      • Assign a priority score.
 *      • Generate actionable feature-improvement suggestions.
 *      • Draft a brief developer ticket description.
 *  - Groups categories into a rolling weekly report.
 *  - Writes processed feedback to .admin-cli/ai/feedback-analysis/.
 *  - Top actionable items are queued for admin review via PermissionGate.
 *
 * All I/O uses ONLY Node.js built-in modules.
 */

const fs     = require('fs');
const path   = require('path');
const crypto = require('crypto');

// ---------------------------------------------------------------------------
// Paths & constants
// ---------------------------------------------------------------------------

const ADMIN_HOME      = process.env.ADMIN_HOME || path.resolve(__dirname, '..', '..', '..', '.admin-cli');
const AI_STATE_DIR    = path.join(ADMIN_HOME, 'ai');
const FEEDBACK_DIR    = path.join(ADMIN_HOME, 'feedback');
const ANALYSIS_DIR    = path.join(AI_STATE_DIR, 'feedback-analysis');

const CATEGORIES = ['bug', 'feature_request', 'ux_issue', 'performance', 'praise', 'other'];

const PRIORITY_LABELS = { high: 3, medium: 2, low: 1 };

/** Max feedback entries to process in a single cycle run. */
const MAX_FEEDBACK_PER_CYCLE = 200;

// ---------------------------------------------------------------------------
// FeedbackProcessor
// ---------------------------------------------------------------------------

class FeedbackProcessor {
    constructor() {
        this._ensureDirs();
        /** @type {object[]} Processed feedback items. */
        this._processed = [];
        /** @type {object[]} Actionable suggestions generated by LLM. */
        this._suggestions = [];
        this._lastRunAt = null;
    }

    // -----------------------------------------------------------------------
    // Public API
    // -----------------------------------------------------------------------

    /**
     * Ingest a new feedback item and save it to disk for later processing.
     *
     * @param {object} feedback  { userId, content, source, metadata? }
     * @returns {string} id of the saved feedback entry
     */
    ingest(feedback) {
        const id = crypto.randomUUID();
        const entry = {
            id,
            userId:    feedback.userId || 'anonymous',
            content:   String(feedback.content || '').slice(0, 4000),
            source:    feedback.source || 'api',
            metadata:  feedback.metadata || {},
            createdAt: new Date().toISOString(),
            processed: false,
        };
        const filePath = path.join(FEEDBACK_DIR, `${id}.json`);
        try { fs.writeFileSync(filePath, JSON.stringify(entry, null, 2), 'utf8'); } catch (_) {}
        return id;
    }

    /**
     * Process all unprocessed feedback entries.
     *
     * @param {Function} [llmFn]   Optional async (prompt:string)=>string.
     * @param {object}   [permGate] PermissionGate for queuing actionable items.
     * @returns {Promise<{processed: number, suggestions: object[]}>}
     */
    async processAll(llmFn, permGate) {
        console.log('[feedback-processor] Processing feedback entries…');
        this._lastRunAt = new Date().toISOString();

        const unprocessed = this._loadUnprocessed();
        console.log(`[feedback-processor] Found ${unprocessed.length} unprocessed entries`);

        if (unprocessed.length === 0) {
            return { processed: 0, suggestions: [] };
        }

        const newSuggestions = [];

        for (const entry of unprocessed) {
            try {
                const result = await this._processEntry(entry, llmFn);
                this._processed.push(result);
                this._markProcessed(entry.id);

                if (result.priority === 'high' || result.category === 'bug') {
                    newSuggestions.push(result);
                }
            } catch (e) {
                console.error(`[feedback-processor] Failed to process ${entry.id}:`, e.message);
            }
        }

        // Keep only last 500 processed items in memory.
        if (this._processed.length > 500) this._processed = this._processed.slice(-500);

        // Generate a weekly summary report with LLM.
        if (llmFn && this._processed.length > 0) {
            const weeklyReport = await this._generateWeeklyReport(llmFn);
            if (weeklyReport) {
                this._saveWeeklyReport(weeklyReport);
                console.log('[feedback-processor] Weekly feedback report generated');
            }
        }

        // Submit high-priority items for admin review.
        if (permGate && newSuggestions.length > 0) {
            const topItems = newSuggestions.slice(0, 5);
            await permGate.requestPermission(
                'review_feedback_suggestions',
                `${topItems.length} high-priority user feedback item(s) need your attention`,
                { items: topItems.map(s => ({ id: s.feedbackId, category: s.category, summary: s.summary })) },
                'medium'
            ).catch(() => {});
        }

        this._suggestions = [...this._suggestions, ...newSuggestions].slice(-200);
        return { processed: unprocessed.length, suggestions: newSuggestions };
    }

    /** Get recent processed feedback. */
    getProcessed(limit = 50) {
        return this._processed.slice(-limit);
    }

    /** Get actionable suggestions. */
    getSuggestions(limit = 20) {
        return this._suggestions.slice(-limit);
    }

    /** Get summary stats. */
    getSummary() {
        const byCat = {};
        for (const p of this._processed) {
            byCat[p.category] = (byCat[p.category] || 0) + 1;
        }
        return {
            lastRunAt: this._lastRunAt,
            total:       this._processed.length,
            byCategory:  byCat,
            suggestions: this._suggestions.length,
            unprocessed: this._countUnprocessed(),
        };
    }

    // -----------------------------------------------------------------------
    // Processing logic
    // -----------------------------------------------------------------------

    /** @private */
    async _processEntry(entry, llmFn) {
        if (!llmFn) {
            return this._ruleBasedProcess(entry);
        }

        const prompt = [
            `You are a product analyst for the Let's Connect / Milonexa social platform.`,
            `Classify and analyze this user feedback.`,
            ``,
            `User feedback:`,
            `"${entry.content}"`,
            ``,
            `Return JSON only with these keys:`,
            `{`,
            `  "category": "${CATEGORIES.join('|')}",`,
            `  "priority": "high|medium|low",`,
            `  "sentiment": "positive|neutral|negative",`,
            `  "summary": "one-sentence summary",`,
            `  "actionable": true/false,`,
            `  "ticketTitle": "short developer ticket title if actionable",`,
            `  "ticketDescription": "clear developer-facing ticket description (2-3 sentences) if actionable"`,
            `}`,
        ].join('\n');

        let parsed = null;
        try {
            const response = await llmFn(prompt);
            const match = response && response.match(/\{[\s\S]*\}/);
            if (match) parsed = JSON.parse(match[0]);
        } catch (_) { /* fall through */ }

        if (!parsed) return this._ruleBasedProcess(entry);

        return {
            feedbackId:          entry.id,
            userId:              entry.userId,
            source:              entry.source,
            originalContent:     entry.content,
            category:            CATEGORIES.includes(parsed.category) ? parsed.category : 'other',
            priority:            PRIORITY_LABELS[parsed.priority] ? parsed.priority : 'low',
            sentiment:           parsed.sentiment || 'neutral',
            summary:             parsed.summary || entry.content.slice(0, 100),
            actionable:          Boolean(parsed.actionable),
            ticketTitle:         parsed.ticketTitle || null,
            ticketDescription:   parsed.ticketDescription || null,
            processedAt:         new Date().toISOString(),
            llmEnhanced:         true,
        };
    }

    /** @private — rule-based fallback when no LLM. */
    _ruleBasedProcess(entry) {
        const text = (entry.content || '').toLowerCase();
        let category = 'other';
        let priority  = 'low';

        if (/bug|error|crash|broken|fail|exception/i.test(text)) { category = 'bug'; priority = 'high'; }
        else if (/feature|add|improve|should|would be nice|wish/i.test(text)) { category = 'feature_request'; priority = 'medium'; }
        else if (/slow|lag|performance|speed/i.test(text)) { category = 'performance'; priority = 'medium'; }
        else if (/confus|difficult|hard to|ux|ui|interface|design/i.test(text)) { category = 'ux_issue'; priority = 'medium'; }
        else if (/great|love|awesome|amazing|good/i.test(text)) { category = 'praise'; priority = 'low'; }

        return {
            feedbackId:     entry.id,
            userId:         entry.userId,
            source:         entry.source,
            originalContent: entry.content,
            category,
            priority,
            sentiment:      category === 'praise' ? 'positive' : category === 'bug' ? 'negative' : 'neutral',
            summary:        entry.content.slice(0, 100),
            actionable:     category !== 'praise',
            ticketTitle:    null,
            ticketDescription: null,
            processedAt:    new Date().toISOString(),
            llmEnhanced:    false,
        };
    }

    /** @private */
    async _generateWeeklyReport(llmFn) {
        const recentItems = this._processed.slice(-100);
        if (recentItems.length === 0) return null;

        const categoryCounts = {};
        const highPriority   = [];
        for (const item of recentItems) {
            categoryCounts[item.category] = (categoryCounts[item.category] || 0) + 1;
            if (item.priority === 'high') highPriority.push(item.summary);
        }

        const prompt = [
            `You are a product manager for the Let's Connect / Milonexa platform.`,
            `Analyze this week's user feedback summary and write an actionable report.`,
            ``,
            `Total feedback items: ${recentItems.length}`,
            `Category breakdown: ${JSON.stringify(categoryCounts)}`,
            `High-priority items: ${highPriority.slice(0, 10).join(' | ')}`,
            ``,
            `Write a concise product feedback report (max 200 words) that includes:`,
            `1. Top user pain points`,
            `2. Most requested features`,
            `3. Three concrete recommendations for the engineering team`,
        ].join('\n');

        try {
            return await llmFn(prompt);
        } catch (_) {
            return null;
        }
    }

    // -----------------------------------------------------------------------
    // File I/O
    // -----------------------------------------------------------------------

    /** @private */
    _loadUnprocessed() {
        const entries = [];
        try {
            const files = fs.readdirSync(FEEDBACK_DIR);
            for (const file of files) {
                if (!file.endsWith('.json')) continue;
                try {
                    const entry = JSON.parse(fs.readFileSync(path.join(FEEDBACK_DIR, file), 'utf8'));
                    if (!entry.processed) entries.push(entry);
                } catch (_) {}
            }
        } catch (_) {}
        return entries.slice(0, MAX_FEEDBACK_PER_CYCLE); // process at most MAX_FEEDBACK_PER_CYCLE per cycle
    }

    /** @private */
    _countUnprocessed() {
        try {
            return fs.readdirSync(FEEDBACK_DIR).filter(f => {
                if (!f.endsWith('.json')) return false;
                try {
                    const e = JSON.parse(fs.readFileSync(path.join(FEEDBACK_DIR, f), 'utf8'));
                    return !e.processed;
                } catch (_) { return false; }
            }).length;
        } catch (_) { return 0; }
    }

    /** @private */
    _markProcessed(id) {
        const filePath = path.join(FEEDBACK_DIR, `${id}.json`);
        try {
            const entry = JSON.parse(fs.readFileSync(filePath, 'utf8'));
            entry.processed = true;
            entry.processedAt = new Date().toISOString();
            fs.writeFileSync(filePath, JSON.stringify(entry, null, 2), 'utf8');
        } catch (_) {}
    }

    /** @private */
    _saveWeeklyReport(content) {
        const week     = this._getISOWeek();
        const filePath = path.join(ANALYSIS_DIR, `week-${week}.md`);
        try {
            fs.writeFileSync(filePath, `# Feedback Report — Week ${week}\n\n${content}\n`, 'utf8');
        } catch (_) {}
    }

    /** @private — returns ISO 8601 week string e.g. "2026-W11". */
    _getISOWeek() {
        const now    = new Date();
        // Thursday in current week determines the year (ISO 8601 rule).
        const thursday = new Date(now);
        thursday.setDate(now.getDate() + (4 - (now.getDay() || 7)));
        const year   = thursday.getFullYear();
        const jan4   = new Date(year, 0, 4); // Jan 4 is always in week 1.
        const week   = Math.ceil(((thursday - jan4) / 86400000 + jan4.getDay() + 1) / 7);
        return `${year}-W${String(week).padStart(2, '0')}`;
    }

    /** @private */
    _ensureDirs() {
        [AI_STATE_DIR, FEEDBACK_DIR, ANALYSIS_DIR].forEach(d => {
            if (!fs.existsSync(d)) fs.mkdirSync(d, { recursive: true });
        });
    }
}

module.exports = { FeedbackProcessor };
