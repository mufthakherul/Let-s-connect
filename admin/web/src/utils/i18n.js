const getLanguage = () => {
  try {
    return localStorage.getItem('admin-lang') || 'en';
  } catch {
    return 'en';
  }
};

export const setLanguage = (lang) => {
  try {
    localStorage.setItem('admin-lang', lang);
  } catch {
    // ignore
  }
};

const t = (key) => {
  const lang = getLanguage();
  const dict = translations[lang] || translations.en;
  return dict[key] ?? key;
};

export const useTranslation = () => {
  return { t };
};
